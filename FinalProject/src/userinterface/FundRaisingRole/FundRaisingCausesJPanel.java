    private void btnAddServiceActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddServiceActionPerformed
        // TODO add your handling code here:
        String name = txtFundName.getText();
        String desc = txtFundDesc.getText();
        int amount = Integer.parseInt(txtFundsRequired.getText());
        String type = txtFundType.getText();

        try {
            if(name == null || name.isEmpty()){
                throw new NullPointerException("Fund Name field cannot be Empty !!!");
            }
        } catch(NullPointerException e){
            JOptionPane.showMessageDialog(null, "Fund Name field cannot be Empty !!!");
            return;
        }

        try {
            if(desc==null || desc.isEmpty()){
                throw new NullPointerException("Description field cannot be Empty !!!");
            }
        } catch(NullPointerException e){
            JOptionPane.showMessageDialog(null, "Description field cannot be Empty !!!");
            return;
        }
        
        try {
            if(type==null || type.isEmpty()){
                throw new NullPointerException("Type field cannot be Empty !!!");
            }
        } catch(NullPointerException e){
            JOptionPane.showMessageDialog(null, "Type field cannot be Empty !!!");
            return;
        }

        try {
            if(amount==0){
                throw new NullPointerException("Amount Field cannot be empty !!!");
            }
//            else if(Pattern.matches("^[0-9]{0,6}$", amount) == false){
//                throw new Exception("Invalid Amount !!!");
//            }
        }  catch(NullPointerException e){
            JOptionPane.showMessageDialog(null, "Amount Field cannot be empty !!!");
            return;
        }
//        catch (Exception e) {
//            JOptionPane.showMessageDialog(null, "Invalid Amount !!!");
//            return;
//        }
        

        for(FundRaising fundRaising:system.getFundRaisingDirectory().getFundRaisingList()){
            if(fundRaising.getUserName().equals(ua.getUsername())){
                funds = system.getFundRaisingDirectory().addFundsTypes(fundRaising, name, desc, type, amount);
            }
        }

        txtFundName.setText("");
        txtFundDesc.setText("");
        txtFundsRequired.setText("");
        txtFundType.setText("");
        populateCausesTable();
    }//GEN-LAST:event_btnAddServiceActionPerformed

