    private void populateEmgTable() {
        DefaultTableModel model = (DefaultTableModel) tblSoupKitchenOrg.getModel();
        model.setRowCount(0);
        
            Object[] row = new Object[4];    
            for(EmergencyDistressOrg org:system.getEmergencyDistressOrgDirectory().getEmergencyDistressOrgList()){
                 row[0] = org;
                 row[1] = org.getEmergencyOrgAddress();
                 row[2] = org.getEmergencyOrgPhNum();
                 row[3] = org.getEmergencyOrgEmail();
                 model.addRow(row);
            }
    }

    private void populateRequestTable() {
        DefaultTableModel model = (DefaultTableModel) tblOrders.getModel();
        model.setRowCount(0);

          for (Member mem:system.getMemberDirectory().getMemberList()) {   
            if (mem.getMemUsername().equals(account.getUsername())) {
               for(EmergencyWorkRequest service:mem.getEmgRequestList()){
                Object[] row = new Object[5];
                row[0] = service;
                row[1] = service.getEmgOrgName();
                row[2] = service.getIssue();
                row[3] = service.getPickupAddress();
                row[4] = service.getStatus();
                model.addRow(row);
               }            
            }  
        }
    }
