    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        String name = txtFundRaisingName.getText();
        String address = txtFundRaisingAddress.getText();
        String number = txtFundRaisingPhNum.getText();
        String email = txtFundRaisingEmail.getText();

        try {
            if(name==null || name.isEmpty()){
                throw new NullPointerException("Fund Raising Org Name Field cannot be Empty !!!");

            }
        } catch(NullPointerException e){
            JOptionPane.showMessageDialog(null, "Fund Raising Org Name Field cannot be Empty !!!");
            return;
        }

        try {
            if(address==null || address.isEmpty()){
                throw new NullPointerException("Fund Raising Org Address Field cannot be Empty !!!");
            }
        } catch(NullPointerException e){
            JOptionPane.showMessageDialog(null, "Fund Raising Org Address Field cannot be Empty !!!");
            return;
        }

        try {

            if(number==null || number.isEmpty()){
                throw new NullPointerException("Fund Raising Org Phone Number Field cannot be Empty !!!");
            }else if(Pattern.matches("^\\(?([0-9]{3})\\)?[-.\\s]?([0-9]{3})[-.\\s]?([0-9]{4})$", number) == false){
                throw new Exception("Enter a Valid Phone number !!!");
            }
        }  catch(NullPointerException e){
            JOptionPane.showMessageDialog(null, "Fund Raising Org Phone Number Field cannot be Empty !!!");
            return;
        }catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Enter a Valid Phone number !!!");
            return;
        }

        try {
            if(email==null || email.isEmpty()){
                throw new NullPointerException("Fund Raising Org Email Field cannot be Empty !!!");

            }
        } catch(NullPointerException e){
            JOptionPane.showMessageDialog(null, "Fund Raising Org Email Field cannot be Empty !!!");
            return;
        }

        for(FundRaising fundRaising:system.getFundRaisingDirectory().getFundRaisingList()){
            if(fundRaising.getUserName().equals(account.getUsername())){
                system.getFundRaisingDirectory().updateFundRaisingInfo(fundRaising, name, number, address, email);
            }
        }

        txtFundRaisingName.setEnabled(false);
        txtFundRaisingAddress.setEnabled(false);
        txtFundRaisingPhNum.setEnabled(false);
        txtFundRaisingEmail.setEnabled(false);
        btnSave.setEnabled(false);
        btnUpdate.setEnabled(true);
    }//GEN-LAST:event_btnSaveActionPerformed

