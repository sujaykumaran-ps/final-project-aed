private void populateLeafOrgTable() {
        
        DefaultTableModel model = (DefaultTableModel) tblLeafClearingOrg.getModel();
        model.setRowCount(0);
        
            Object[] row = new Object[4];    
            for(LeafClearingOrg org:system.getLeafClearingOrgDirectory().getLeafClearingOrgList()){
                 row[0] = org;
                 row[1] = org.getLeafOrgAddress();
                 row[2] = org.getLeafOrgPhNum();
                 row[3] = org.getLeafOrgEmail();
                 model.addRow(row);
            }
            
    }

    public void populateLeafRequestsTable() {
        
        DefaultTableModel model = (DefaultTableModel) tblRequests.getModel();
        model.setRowCount(0);

          for (Member mem:system.getMemberDirectory().getMemberList()) {   
            if (mem.getMemUsername().equals(account.getUsername())) {
               for(LeafWorkRequest service:mem.getLeafRequestList()){
                Object[] row = new Object[4];
                row[0] = service;
                row[1] = service.getLeafOrgName();
                row[2] = service.getServiceAddress();
                row[3] = service.getStatus();
                model.addRow(row);
               }            
            }  
        }
    }
